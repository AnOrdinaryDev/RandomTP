package com.gmail.theposhogamer.Util;

public class CustomLoc {
	
	int x;
	int y;
	int z;
	String world;
	int blocks;
	int price;
	int cooldown;
	
	public CustomLoc(int x, int y, int z, String world, int blocks, int price, int cooldown) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.world = world;
		this.blocks = blocks;
		this.price = price;
		this.cooldown = cooldown;
	}
	
}
